     */
    public function store(Request $request)
    {
        $request->validate([
            'text' => 'required',
            'post_id' => 'required|integer|exists:post,id',

        ]);
        auth()->user()->comments()->create(
            $request->all()
        );

        return redirect('/posts/' . $request->post_id . '#comments');
    }

   

